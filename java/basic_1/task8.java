//Pattern Display: JAVA
//
//Write a Java program to display the following pattern.
//
//Sample Pattern :
//
//J    a   v     v  a
//J   a a   v   v  a a
//J  J  aaaaa   V V  aaaaa
//JJ  a     a   V  a     a


public class task8 {
    public static void main(String[] args) {
        System.out.println("    J  AAAAA  v      v AAAAAA");
        System.out.println("    J  A   A   v    v  A    A");
        System.out.println("J   J  AAAAA    V  V   AAAAAA");
        System.out.println("JJJJJ  A   A     V     A    A");
    }
}

/*
    Arithmetic Operations

    Write a Java program to print the results of the following operations.
*/

public class Exercise4 {
    public static void main(String[] args) {
        int value1 = 10;
        int value2 = 2;
        System.out.println("Sum:" +  value1 + value2);
        System.out.println("Reduce:" + (value1 - value2));
        System.out.println("Multiplication:" + (value1 * value2));
        System.out.println("division:" + (value1 / value2));
        System.out.println("rest:" + (value1 % value2));
    }
}

/*
    Division of Two Numbers

    Write a Java program to divide two numbers and print them on the screen.

    Division is one of the four basic operations of arithmetic, the others being addition, subtraction,
    and multiplication. The division of two natural numbers is the process of calculating the number of
    times one number is contained within one another.
*/

public class Exercise3 {
    public static void main(String[] args) {
        int value = 24;
        int division = 4;
        double result = (double) value / division;

        System.out.println(result);
    }
}
